#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	size_t	i;
	size_t	len;
	char	*str;

	if (s && f)
	{
		i = 0;
		len = ft_strlen((char *)s);
		str = (char *)malloc(sizeof(*str) * len + 1);
		if (!str)
			return (NULL);
		while (s[i] != '\0')
		{
			str[i] = f(s[i]);
			i++;
		}
		str[i] = '\0';
		return (str);
	}
	return (NULL);
}
