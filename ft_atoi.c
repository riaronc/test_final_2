#include "libft.h"

int		ft_atoi(const char *s)
{
	size_t			res;
	int				i;
	int				sign;

	i = 0;
	res = 0;
	sign = 1;
	while ((s[i] >= 8 && s[i] <= 14) || s[i] == ' ')
		i++;
	if (s[i] == '-')
		sign = -1;
	if (s[i] == '-' || s[i] == '+')
		i++;
	while (s[i] >= '0' && s[i] <= '9')
	{
		if (res >= 9223372036854775807 && sign == 1)
			return (-1);
		else if (res > 9223372036854775807 && sign == -1)
			return (0);
		res = 10 * res + (s[i] - '0');
		i++;
	}
	return (res * sign);
}
