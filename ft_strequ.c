#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int i;
	int l1;
	int l2;

	if (s1 && s2)
	{
		i = 0;
		l1 = ft_strlen(s1);
		l2 = ft_strlen(s2);
		if (l1 == l2)
		{
			while (s1[i] == s2[i] && s1[i] != '\0')
				i++;
			if (s1[i] == '\0')
				return (1);
		}
	}
	return (0);
}
