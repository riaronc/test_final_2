#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char *num;

	if (n == -2147483648)
		ft_putstr_fd("-2147483648", fd);
	else
	{
		num = ft_itoa(n);
		ft_putstr_fd(num, fd);
	}
}
